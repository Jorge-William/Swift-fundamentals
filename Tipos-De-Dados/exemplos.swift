// Exemplos de Tipos de Dados em Swift

// Int
let idade: Int = 25
let ano = 2025 // Inferência como Int

// Double
let pi: Double = 3.14159
let temperatura = 36.6 // Inferência como Double

// Float
let altura: Float = 1.75

// Bool
let ativo: Bool = true
let aprovado = false

// String
let nome: String = "Maria"
let saudacao = "Olá, mundo!"

// Character
let inicial: Character = "M"

// Array
let numeros: [Int] = [1, 2, 3, 4]
let nomes = ["Ana", "Bruno", "Carlos"]

// Dictionary
let capitais: [String: String] = ["Brasil": "Brasília", "França": "Paris"]

// Set
let vogais: Set<Character> = ["a", "e", "i", "o", "u"]

// Optional
var nota: Double? = nil
nota = 8.5
